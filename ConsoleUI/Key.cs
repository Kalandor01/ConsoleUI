namespace ConsoleUI
{
    /// <summary>
    /// Required responses for the <c>KeyAction</c> object when used in one of the the UI objects/functions.
    /// </summary>
    public enum Key:int
    {
        /// <summary>
        /// Key identifier that corespons to the escape action.
        /// </summary>
        ESCAPE = 0,
        /// <summary>
        /// Key identifier that corespons to the up action.
        /// </summary>
        UP = 1,
        /// <summary>
        /// Key identifier that corespons to the down action.
        /// </summary>
        DOWN = 2,
        /// <summary>
        /// Key identifier that corespons to the left action.
        /// </summary>
        LEFT = 3,
        /// <summary>
        /// Key identifier that corespons to the right action.
        /// </summary>
        RIGHT = 4,
        /// <summary>
        /// Key identifier that corespons to the enter action.
        /// </summary>
        ENTER = 5
    }
}
